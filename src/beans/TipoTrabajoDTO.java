package beans;

public class TipoTrabajoDTO {
	private String cod_tiptrabajo;
	private String des_tiptrabajo;
	private String estado_tiptrabajo;
	private String fecha_reg_tiptrabajo;

	public String getCod_tiptrabajo() {
		return cod_tiptrabajo;
	}

	public void setCod_tiptrabajo(String cod_tiptrabajo) {
		this.cod_tiptrabajo = cod_tiptrabajo;
	}

	public String getDes_tiptrabajo() {
		return des_tiptrabajo;
	}

	public void setDes_tiptrabajo(String des_tiptrabajo) {
		this.des_tiptrabajo = des_tiptrabajo;
	}

	public String getEstado_tiptrabajo() {
		return estado_tiptrabajo;
	}

	public void setEstado_tiptrabajo(String estado_tiptrabajo) {
		this.estado_tiptrabajo = estado_tiptrabajo;
	}

	public String getFecha_reg_tiptrabajo() {
		return fecha_reg_tiptrabajo;
	}

	public void setFecha_reg_tiptrabajo(String fecha_reg_tiptrabajo) {
		this.fecha_reg_tiptrabajo = fecha_reg_tiptrabajo;
	}

}
